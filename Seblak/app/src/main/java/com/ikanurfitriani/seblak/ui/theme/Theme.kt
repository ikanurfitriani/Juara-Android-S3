// Anotasi yang menandakan bahwa fungsi SeblakTheme adalah komponen Composable
@Composable
// Fungsi SeblakTheme dengan beberapa parameter
fun SeblakTheme(
    // Menentukan tema yang digunakan gelap atau terang
    darkTheme: Boolean = isSystemInDarkTheme(),
    // Menentukan penggunaan warna dinamis diaktifkan atau tidak
    dynamicColor: Boolean = false,
    // Fungsi composable tanpa parameter untuk menentukan tata letak yang akan diatur
    content: @Composable () -> Unit
) {
    // Menentukan skema warna berdasarkan kondisi tertentu
    val colorScheme = when {
        // Jika dynamicColor diaktifkan dan perangkat menjalankan Android 12+, maka skema warna dinamis akan diambil menggunakan fungsi dynamicDarkColorScheme atau dynamicLightColorScheme.
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        // Jika tidak, maka akan menggunakan skema warna statis dari DarkColors atau LightColors sesuai dengan darkTheme.
        darkTheme -> DarkColors
        else -> LightColors
    }
    // Untuk mendapatkan referensi ke View
    val view = LocalView.current
    // Menggunakan SideEffect untuk mengeksekusi kode efek samping.
    // Kode ini mengatur warna latar status bar pada perangkat untuk mencocokkan warna utama dari skema warna yang digunakan.
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.primary.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = darkTheme
        }
    }

    // Untuk menerapkan tema dengan drama warna telat status bar
    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}